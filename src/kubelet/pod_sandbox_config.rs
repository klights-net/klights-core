use crate::kubelet::pod_dns::parse_host_resolv_conf;
use crate::kubelet::pod_hosts::{parse_boolish, resolve_hostname};
use k8s_cri::v1::{
    DnsConfig, Int64Value, LinuxPodSandboxConfig, LinuxSandboxSecurityContext, NamespaceOption,
    PodSandboxConfig, PodSandboxMetadata,
};
use serde_json::Value;

/// Build sandbox config with dnsPolicy support.
/// - dnsPolicy: "ClusterFirst" (default) → use cluster DNS
/// - dnsPolicy: "Default" → use host's /etc/resolv.conf
/// - dnsPolicy: "None" → empty DNS config (or use spec.dnsConfig if provided)
/// - dnsPolicy: "ClusterFirstWithHostNet" → same as ClusterFirst
pub fn build_sandbox_config_with_dns_policy(
    pod_name: &str,
    namespace: &str,
    _pod_ip: &str,
    pod_uid: &str,
    containerd_ns: &str,
    dns_ip: &str,
    pod_spec: &Value,
) -> PodSandboxConfig {
    // Detect rootless mode: inside a user namespace, uid_map shows a non-trivial
    // mapping (not "0 0 4294967295").  In rootless mode the process cannot create
    // cgroups under /sys/fs/cgroup/ so we leave cgroup_parent empty.
    let rootless = std::fs::read_to_string("/proc/self/uid_map")
        .map(|m| {
            let fields: Vec<&str> = m.split_whitespace().collect();
            !(fields.len() >= 3
                && fields[0] == "0"
                && fields[1] == "0"
                && fields[2] == "4294967295")
        })
        .unwrap_or(false);
    let cgroup_parent = if rootless {
        String::new()
    } else {
        // Use containerd-namespace-specific cgroup hierarchy for isolation from K3s.
        // K3s manages /kubepods.slice/ and will SIGKILL unknown pods in that tree.
        // Each klights instance uses /{containerd_ns}/ as its cgroup root.
        //
        // Plain cgroupfs path (no `.slice` suffix) — containerd is configured with
        // `SystemdCgroup = false`, so systemd is bypassed entirely. Using `.slice`
        // names would cause systemd to interpret the directories as slice units it
        // owns (systemd parses cgroup paths by suffix in cg_path_decode_unit), which
        // can lead to controller reclamation under resource pressure.
        format!(
            "/{ns}/besteffort/pod{uid}",
            ns = containerd_ns,
            uid = pod_uid
        )
    };

    // Extract dnsPolicy from pod spec (defaults to "ClusterFirst")
    let dns_policy = pod_spec
        .get("dnsPolicy")
        .and_then(|p| p.as_str())
        .unwrap_or("ClusterFirst");

    // Build DNS config based on dnsPolicy
    let dns_config = match dns_policy {
        "Default" => {
            // Use host's /etc/resolv.conf
            let (nameservers, searches, options) = parse_host_resolv_conf();
            Some(DnsConfig {
                servers: nameservers,
                searches,
                options,
            })
        }
        "None" => {
            // Empty DNS config (could be overridden by spec.dnsConfig)
            Some(DnsConfig {
                servers: vec![],
                searches: vec![],
                options: vec![],
            })
        }
        _ => {
            // Use cluster DNS (default behavior for ClusterFirst, ClusterFirstWithHostNet, and others)
            Some(DnsConfig {
                servers: vec![dns_ip.to_string()],
                searches: vec![
                    format!("{}.svc.cluster.local", namespace),
                    "svc.cluster.local".to_string(),
                    "cluster.local".to_string(),
                ],
                options: vec!["ndots:5".to_string()],
            })
        }
    };

    // Merge spec.dnsConfig into the base DNS config (K8s spec: dnsConfig fields
    // are merged with the DNS config generated by dnsPolicy)
    let dns_config = if let Some(dns_cfg) = pod_spec.get("dnsConfig") {
        let mut dns = dns_config.unwrap_or(DnsConfig {
            servers: vec![],
            searches: vec![],
            options: vec![],
        });

        // Merge nameservers (custom nameservers added to list)
        if let Some(ns_array) = dns_cfg.get("nameservers").and_then(|v| v.as_array()) {
            for ns in ns_array {
                if let Some(s) = ns.as_str()
                    && !dns.servers.contains(&s.to_string())
                {
                    dns.servers.push(s.to_string());
                }
            }
        }

        // Merge searches (custom searches appended)
        if let Some(s_array) = dns_cfg.get("searches").and_then(|v| v.as_array()) {
            for s in s_array {
                if let Some(domain) = s.as_str()
                    && !dns.searches.contains(&domain.to_string())
                {
                    dns.searches.push(domain.to_string());
                }
            }
        }

        // Merge options (custom options appended; format: {"name": "x", "value": "y"} → "x:y")
        if let Some(o_array) = dns_cfg.get("options").and_then(|v| v.as_array()) {
            for opt in o_array {
                let name = opt.get("name").and_then(|n| n.as_str()).unwrap_or("");
                let value = opt.get("value").and_then(|v| v.as_str());
                let option_str = if let Some(v) = value {
                    format!("{}:{}", name, v)
                } else {
                    name.to_string()
                };
                // Replace existing option with same name prefix, or append
                let existing_idx = dns
                    .options
                    .iter()
                    .position(|o| o.starts_with(&format!("{}:", name)) || o == name);
                if let Some(idx) = existing_idx {
                    dns.options[idx] = option_str;
                } else if !option_str.is_empty() {
                    dns.options.push(option_str);
                }
            }
        }

        Some(dns)
    } else {
        dns_config
    };

    let host_network = parse_boolish(pod_spec.get("hostNetwork"));
    let host_pid = parse_boolish(pod_spec.get("hostPID"));
    let host_ipc = parse_boolish(pod_spec.get("hostIPC"));

    // For hostNetwork pods, avoid forcing hostname at sandbox creation.
    // On this implementation, hostNetwork pods are placed in host network namespace and
    // setting sandbox hostname can fail in runtimes without a private UTS namespace
    // (e.g. `failed to set hostname without a private UTS namespace`).
    let hostname = if host_network {
        String::new()
    } else {
        // Default behavior for non-hostNetwork pods: spec.hostname (or pod name fallback).
        resolve_hostname(pod_spec, pod_name)
    };

    let mut sandbox_sysctls = std::collections::HashMap::new();
    if let Some(sysctls) = pod_spec
        .pointer("/securityContext/sysctls")
        .and_then(|v| v.as_array())
    {
        for entry in sysctls {
            let Some(name) = entry.get("name").and_then(|v| v.as_str()) else {
                continue;
            };
            if name.is_empty() {
                continue;
            }
            let Some(value) = entry.get("value") else {
                continue;
            };
            if let Some(value_str) = value
                .as_str()
                .map(str::to_string)
                .or_else(|| value.as_i64().map(|n| n.to_string()))
                .or_else(|| value.as_u64().map(|n| n.to_string()))
                .or_else(|| value.as_bool().map(|b| b.to_string()))
            {
                sandbox_sysctls.insert(name.to_string(), value_str);
            }
        }
    }

    PodSandboxConfig {
        metadata: Some(PodSandboxMetadata {
            name: pod_name.to_string(),
            namespace: namespace.to_string(),
            uid: pod_uid.to_string(),
            ..Default::default()
        }),
        hostname,
        log_directory: crate::paths::pod_log_dir_path(containerd_ns, namespace, pod_name, pod_uid)
            .to_string_lossy()
            .into_owned(),
        dns_config,
        port_mappings: vec![],
        labels: std::collections::HashMap::new(),
        annotations: std::collections::HashMap::new(),
        linux: Some(LinuxPodSandboxConfig {
            cgroup_parent,
            security_context: {
                let pod_sc = pod_spec.get("securityContext");
                let run_as_user = pod_sc
                    .and_then(|sc| sc.get("runAsUser").and_then(|v| v.as_i64()))
                    .map(|value| Int64Value { value });
                let run_as_group = pod_sc
                    .and_then(|sc| sc.get("runAsGroup").and_then(|v| v.as_i64()))
                    .map(|value| Int64Value { value });

                // Check if any container (including init containers) requests privileged mode
                // If so, the sandbox must also be privileged for containerd to allow it
                let any_privileged = pod_spec
                    .get("containers")
                    .and_then(|c| c.as_array())
                    .map(|containers| {
                        containers.iter().any(|c| {
                            c.pointer("/securityContext/privileged")
                                .and_then(|v| v.as_bool())
                                .unwrap_or(false)
                        })
                    })
                    .unwrap_or(false)
                    || pod_spec
                        .get("initContainers")
                        .and_then(|c| c.as_array())
                        .map(|containers| {
                            containers.iter().any(|c| {
                                c.pointer("/securityContext/privileged")
                                    .and_then(|v| v.as_bool())
                                    .unwrap_or(false)
                            })
                        })
                        .unwrap_or(false);

                Some(LinuxSandboxSecurityContext {
                    namespace_options: Some(NamespaceOption {
                        network: if host_network { 2 } else { 0 }, // NODE or POD
                        pid: if host_pid { 2 } else { 0 },         // NODE or POD
                        ipc: if host_ipc { 2 } else { 0 },         // NODE or POD
                        ..Default::default()
                    }),
                    run_as_user,
                    run_as_group,
                    privileged: any_privileged,
                    ..Default::default()
                })
            },
            sysctls: sandbox_sysctls,
            resources: None,
            overhead: None,
        }),
        windows: None,
    }
}

