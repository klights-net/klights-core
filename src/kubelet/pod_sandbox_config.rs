    #[test]
    fn test_build_sandbox_config_dns_policy_default() {
        // dnsPolicy: Default should use host's /etc/resolv.conf nameservers
        let pod_spec = serde_json::json!({
            "dnsPolicy": "Default"
        });
        let config = build_sandbox_config_with_dns_policy(
            "coredns",
            "kube-system",
            "",
            "uid-coredns",
            "klights",
            "10.43.128.10",
            &pod_spec,
        );
        let dns = config.dns_config.unwrap();

        // Should NOT use cluster DNS IP
        assert_ne!(
            dns.servers,
            vec!["10.43.128.10"],
            "Should not use cluster DNS for dnsPolicy: Default"
        );

        // Should use host nameservers (we'll parse /etc/resolv.conf)
        // At minimum, should have at least one nameserver
        assert!(
            !dns.servers.is_empty(),
            "Should have at least one nameserver from host"
        );

        // Search domains should be empty or from host, not cluster domains
        assert!(
            !dns.searches.contains(&"svc.cluster.local".to_string()),
            "Should not have cluster search domains for dnsPolicy: Default"
        );
    }

