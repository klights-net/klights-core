    #[test]
    fn test_build_sandbox_config_dns_config_merges_custom_searches() {
        // Sonobuoy test: "customized DNS suffix list" — spec.dnsConfig.searches
        // must be appended to the base search domains from dnsPolicy.
        let pod_spec = serde_json::json!({
            "dnsPolicy": "ClusterFirst",
            "dnsConfig": {
                "searches": ["resolv.conf.local"],
                "nameservers": ["192.0.2.4"],
                "options": [{"name": "timeout", "value": "3"}]
            }
        });
        let config = build_sandbox_config_with_dns_policy(
            "dns-test-pod",
            "dns-8515",
            "",
            "uid-dns",
            "klights",
            "10.43.128.10",
            &pod_spec,
        );
        let dns = config.dns_config.unwrap();

        // Custom nameservers prepended
        assert!(
            dns.servers.contains(&"192.0.2.4".to_string()),
            "dnsConfig.nameservers should be merged, got: {:?}",
            dns.servers
        );

        // Custom searches appended after cluster defaults
        assert!(
            dns.searches.contains(&"resolv.conf.local".to_string()),
            "dnsConfig.searches should be appended, got: {:?}",
            dns.searches
        );
        assert_eq!(
            dns.searches[0], "dns-8515.svc.cluster.local",
            "ClusterFirst search domains should come first"
        );

        // Custom options merged
        assert!(
            dns.options.iter().any(|o| o.contains("timeout")),
            "dnsConfig.options should be merged, got: {:?}",
            dns.options
        );
    }

